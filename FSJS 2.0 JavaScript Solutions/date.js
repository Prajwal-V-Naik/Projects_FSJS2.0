// - What is the year today?
let A = new Date();
let B = A.getFullYear();
console.log(B)
// Then Output be like:
// 2023


// - What is the month today as a number?
let C = A.getMonth();
console.log(C);
// Then Output be like:
// 0


//  - What is the date today?
let D = A.getDate();
console.log(D);
// Then Output be like:
// 17


// - What is the day today as a number?
let E = A.getDay();
console.log(E);
// Then Output be like:
// 2


// - What is the hours now?
let F = A.getHours();
console.log(F);
// Then Output be like:
// 9

